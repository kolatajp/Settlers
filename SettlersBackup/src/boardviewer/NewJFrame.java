        int y = evt.getY();
        String txt = "";
        
        DrawableObject list[] = this.bvpm.dpm.getObjects( x, y );
        for ( int i=0; i<list.length; i++ ) {
            txt += list[i].getId() + "\n";
        }
        
        this.textArea1.setText( txt );
